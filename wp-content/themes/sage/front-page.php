      <div class="row">
        <div class="col-md-6">
          In 2003 I was one of six pre-med students selected nationwide by the University of Maryland School of Medicine to intern in a medical research program to develop a vaccine against Malaria in Mali, West Africa. As a naive 20 year old college student who dreamed of changing the world, that was the greatest honor of my life and a humbling lesson on Giving Back.

Fast track to nine years later and a MBA in hand, I quit my six figure job, tapped into my savings and along with Josh and John co-founded Miigle, a platform that allowed people to discover and foster innovative projects around the world that matched their interests. Our goal was to empower the public to help not just innovators raising money, but also those needing emotional, practical, and intellectual support, especially in impoverished regions around the world.

We launched Miigle at 2014 The Launch Festival in San Francisco, and were told by the judges on stage, Silicon Valley Investors, that we would never succeed because we weren’t part of the “Boys Club” and money gets you everything. Watch the video

Did it hurt? Terribly. But we learned that we had to do better and most importantly that there are millions of people working hard every day to make this world a better place and we didn’t want to sit on the sidelines.


